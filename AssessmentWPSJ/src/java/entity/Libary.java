package entity;

/**
 *
 * @author NamIT
 */
public class Libary {
    int code;
    String name;
    String auther;
    boolean bon;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAuther() {
        return auther;
    }

    public void setAuther(String auther) {
        this.auther = auther;
    }

    public boolean isBon() {
        return bon;
    }

    public void setBon(boolean bon) {
        this.bon = bon;
    }
    
    
}
